package JavaBasics;

public class LoopsConcept {

	public static void main(String[] args) {

		//1. While Loop
		int i = 1;
		
		while (i <= 10) {
			System.out.println("The value of i = " + i);
			i++;
		}
		
		System.out.println("-----------------------------");
		
		//2. For Loop
		for(int j=0; j<=10; j++) {
			System.out.println("The value of j = " + j);
		}
		
		System.out.println("-----------------------------");
		
		for(int k=10; k>=-10; k--) {
			System.out.println("The value of k = " + k);
		}
		
		System.out.println("-----------------------------");
		
		
		
		
	}

}
